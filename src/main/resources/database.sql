CREATE DATABASE 613801_shavlovskii1;
USE 613801_shavlovskii1;
CREATE TABLE cars (
  id INT(6) UNSIGNED AUTO_INCREMENT PRIMARY KEY,
  mark VARCHAR(500) NOT NULL,
  model VARCHAR(500) NOT NULL,
  date VARCHAR(500) NOT NULL,
  motor VARCHAR(500) NOT NULL,
  fuel VARCHAR(500) NOT NULL,
  color VARCHAR(500) NOT NULL,
  cost VARCHAR(500) NOT NULL
);